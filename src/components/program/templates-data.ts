import { LocalDay, LocalExercise, genId } from "./types";

export interface ProgramTemplate {
  id: string;
  name: string;
  goal: string;
  level: string;
  weeks: number;
  daysPerWeek: number;
  equipment: string;
  description: string;
  category: string;
  days: LocalDay[];
}

const ex = (
  name: string, muscle: string, sets: number, reps: number,
  weight: number, rest: number, rpe: number | null = null,
  tempo = "", notes = ""
): LocalExercise => ({
  id: genId(), name, muscle, sets, reps, weight,
  video_url: "", rest_seconds: rest, tempo, rpe,
  notes, supersetWith: undefined, is_warmup: false,
});

const warmup = (name: string, muscle: string, sets: number, reps: number, rest = 30): LocalExercise => ({
  id: genId(), name, muscle, sets, reps, weight: 0,
  video_url: "", rest_seconds: rest, tempo: "", rpe: null,
  notes: "", supersetWith: undefined, is_warmup: true,
});

const restDay = (dayName: string): LocalDay => ({
  dayName, isRest: true, exercises: [], warmup: [], label: "راحة",
});

const day = (dayName: string, label: string, exercises: LocalExercise[], warmups: LocalExercise[] = []): LocalDay => ({
  dayName, isRest: false, label, exercises, warmup: warmups,
});

